import utils.chargrid as g


def go(input:str, start=None) -> g.PosXy:
    if start is None:
        start = (0, 0)
    num = 0
    pos = start
    facing = g.NORTH
    visited = set([pos])
    for x in input.split(','):
        if 'L' in x:
            num = int(x.replace('L', ''))
            facing = g.turnleft(facing)
        elif 'R' in x:
            num = int(x.replace('R', ''))
            facing = g.turnright(facing)
        else:
            raise KeyError("asdfasdf")
        revisited=False
        for _ in range(num):
            pos = g.add(pos, facing, 1)
            if pos in visited:
                revisited = True
                break
            else:
                visited.add(pos)
        if revisited:
            break
    return pos


